package org.cS2114.groupProject.items;

// -------------------------------------------------------------------------
/**
 * Defines the class of that a specific item falls into.
 *
 * @author Tim Eck (etimot2)
 * @version 2012.4.9
 */
public enum ItemType
{
    /**
     * Defines a potion item
     */
    Potion,
    /**
     * Defines an armor item type
     */
    Armor,
    /**
     * Defines a weapon item type
     */
    Weapon,
    /**
     * Item type for an item that does not fit into one of the above types.
     */
    Other
}
